package model;

public class Group {
	private int groupid;
	private String groupname;
	private String groupdesribe;
	/**
	 * @return the groupid
	 */
	public int getGroupid() {
		return groupid;
	}
	/**
	 * @param groupid the groupid to set
	 */
	public void setGroupid(int groupid) {
		this.groupid = groupid;
	}
	/**
	 * @return the groupname
	 */
	public String getGroupname() {
		return groupname;
	}
	/**
	 * @param groupname the groupname to set
	 */
	public void setGroupname(String groupname) {
		this.groupname = groupname;
	}
	/**
	 * @return the groupdesribe
	 */
	public String getGroupdesribe() {
		return groupdesribe;
	}
	/**
	 * @param groupdesribe the groupdesribe to set
	 */
	public void setGroupdesribe(String groupdesribe) {
		this.groupdesribe = groupdesribe;
	}
}
