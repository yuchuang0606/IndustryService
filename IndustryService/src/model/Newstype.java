package model;

public class Newstype {
	private int typeid;
	private int typename;
	/**
	 * @return the itemid
	 */
	public int getTypeid() {
		return typeid;
	}
	public void setTypeid(int typeid) {
		this.typeid = typeid;
	}
	public int getTypename() {
		return typename;
	}
	public void setTypename(int typename) {
		this.typename = typename;
	}
	

}